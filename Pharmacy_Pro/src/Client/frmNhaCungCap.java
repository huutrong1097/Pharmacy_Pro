    private boolean validation() {
        if (txtMaNCC.getText().equals("") || txtTenNCC.getText().equals("")
                || txtDiaChi.getText().equals("") || txtDienThoai.getText().equals("")){
            return false;
        }
        return true;
    }
    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        String maNCC = txtMaNCC.getText();
        String tenNCC = txtTenNCC.getText();
        String diaChiNCC = txtDiaChi.getText();
        String dienThoaiNCC = txtDienThoai.getText();
        ncc = new NhaCC(maNCC, tenNCC, diaChiNCC, dienThoaiNCC);
        if (validation()) {
            try {
                if (!rmi_client.user.KiemTraSo(dienThoaiNCC)) {
                    JOptionPane.showMessageDialog(null, "Số điện thoại phải là số!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    int res = rmi_client.user.ThemNCC(ncc);
                    if (res > 0) {
                        JOptionPane.showMessageDialog(null, "Lưu thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        dtm.setRowCount(0);
                        Show_Data_NCC();
                        
                    } else {
                        JOptionPane.showMessageDialog(null, "Đã tồn tại Mã nhà cung cấp", "Warning", JOptionPane.WARNING_MESSAGE);
                    }
                }
            } catch (RemoteException ex) {
                Logger.getLogger(frmNhaCungCap.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Bạn chưa điền đủ thông tin", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnLuuActionPerformed

