    private void Show_Data_NCC() {
        try {
            ArrayList<NhaCC> ncclist = rmi_client.user.LoadData_NCC();
            dtm = (DefaultTableModel) tableNhaCC.getModel();//thêm dữ liêu vào tablenguoidung
            Object[] obj = new Object[4];
            for (int i = 0; i < ncclist.size(); i++) {
                obj[0] = ncclist.get(i).getMaNCC();
                obj[1] = ncclist.get(i).getTenNCC();
                obj[2] = ncclist.get(i).getDiaChiNCC();
                obj[3] = ncclist.get(i).getDienThoaiNCC();
                dtm.addRow(obj);//Thêm một dòng vào dm
            }
        } catch (RemoteException e) {
            JOptionPane.showMessageDialog(null, "Lỗi " + e);

        }
    }

