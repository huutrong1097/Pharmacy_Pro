    private boolean validation() {
        if (txtMaThuoc.getText().equals("") || txtTenThuoc.getText().equals("")
                || txtSoLuong.getText().equals("") || txtDonGiaBan.getText().equals("")
                || txtDonGiaMua.getText().equals("")) {
            return false;
        }
        return true;
    }
    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed

        String maThuoc = txtMaThuoc.getText();
        String tenThuoc = txtTenThuoc.getText();
        String maDVT = cboMaDVT.getSelectedItem().toString();
        String soLuong = txtSoLuong.getText();
        String donGiaMua = txtDonGiaMua.getText();
        String donGiaBan = txtDonGiaBan.getText();
        String maLThuoc = cboMaLoaiThuoc.getSelectedItem().toString();
        if (validation()) {
            try {
                th = new Thuoc(maThuoc, tenThuoc, maDVT, Integer.parseInt(soLuong), Double.parseDouble(donGiaMua), Double.parseDouble(donGiaBan), maLThuoc);
                int res = rmi_client.user.ThemThuoc(th);
//                System.err.println(res);
                if (res > 0) {
                    JOptionPane.showMessageDialog(null, "Lưu thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                    dtm.setRowCount(0);
                    ShowData();
                } else {
                    JOptionPane.showMessageDialog(null, "Đã tồn tại mã thuốc", "Warning", JOptionPane.WARNING_MESSAGE);
                }
            } catch (RemoteException ex) {
                Logger.getLogger(frmThuoc.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Bạn chưa điền đủ thông tin", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int dongdachon = tableThuoc.getSelectedRow();
        if (dongdachon == -1) {
            JOptionPane.showMessageDialog(null, "Bạn chưa chọn dòng cần xóa");
        } else {
            int[] index = tableThuoc.getSelectedRows();//lấy chỉ mục trong bảng khi người dùng click để xóa
            for (int i = 0; i < index.length; i++) {
                String indexDelete = tableThuoc.getValueAt(index[i], 0).toString();//chuyển sang chuỗi
                try {
                    rmi_client.user.XoaThuoc(indexDelete);//đưa qua server xử lý xóa dữ liệu trong CSDL
                } catch (RemoteException ex) {
                    Logger.getLogger(frmThuoc.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            dtm.setRowCount(0);//đặt số dòng về 0
            ShowData();//load lại dữ liệu vào bảng sau khi xóa
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnXoaActionPerformed

