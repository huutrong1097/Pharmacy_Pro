    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String search = txtTimKiem.getText();
        try {

            ArrayList<Thuoc> listThuoc = rmi_client.user.TimKiemThuoc(search);
            dtm = (DefaultTableModel) tableThuoc.getModel();//thêm dữ liêu vào tablenguoidung
            dtm.setRowCount(0);
            Object[] obj = new Object[7];
            for (int i = 0; i < listThuoc.size(); i++) {
                obj[0] = listThuoc.get(i).getMaThuoc();
                obj[1] = listThuoc.get(i).getTenThuoc();
                obj[2] = listThuoc.get(i).getMaDVT();
                obj[3] = listThuoc.get(i).getSoLuong();
                obj[4] = listThuoc.get(i).getDonGiaMua();
                obj[5] = listThuoc.get(i).getDonGiaBan();
                obj[6] = listThuoc.get(i).getMaLoaiThuoc();
                dtm.addRow(obj);//Thêm một dòng vào dm
            }
        } catch (RemoteException e) {
            JOptionPane.showMessageDialog(null, "Lỗi " + e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnTimKiemActionPerformed

