    private void Show_Data_KH() {
        try {
            ArrayList<KhachHang> listKH = rmi_client.user.LoadData_KH();
            dtm = (DefaultTableModel) tableKhachHang.getModel();//thêm dữ liêu vào tablenguoidung
            Object[] obj = new Object[5];
            for (int i = 0; i < listKH.size(); i++) {
                obj[0] = listKH.get(i).getMaKH();
                obj[1] = listKH.get(i).getTenKH();
                obj[2] = listKH.get(i).getDiaChiKH();
                obj[3] = listKH.get(i).getDienThoaiKH();
                obj[4] = listKH.get(i).getMaLoaiKH();
                dtm.addRow(obj);//Thêm một dòng vào dm
            }
        } catch (RemoteException e) {
            JOptionPane.showMessageDialog(null, "Lỗi " + e);

        }
    }
    private void Show_Data_ComboBox() {
        try {
            ArrayList<LoaiKhachHang> listKH = rmi_client.user.LoadCombobox_KH();
            for (int i = 0; i < listKH.size(); i++) {
                cboMaLoaiKH.addItem(listKH.get(i).getMaLoaiKH());
            }
        } catch (RemoteException e) {
            JOptionPane.showMessageDialog(null, "Lỗi " + e);

        }
    }
