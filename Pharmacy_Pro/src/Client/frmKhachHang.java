    private boolean validation() {
        if (txtMaKH.getText().equals("") || txtTenKH.getText().equals("")
                || txtDiaChi.getText().equals("") ||txtDienThoai.getText().equals("")){
            return false;
        }
        return true;
    }
    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        String maKH = txtMaKH.getText();
        String tenKH = txtTenKH.getText();
        String diaChi = txtDiaChi.getText();
        String dienThoai = txtDienThoai.getText();
        String maLoaiKH=cboMaLoaiKH.getSelectedItem().toString();
        kH = new KhachHang(maKH, tenKH, diaChi, dienThoai,maLoaiKH);
        if (validation()) {
            try {
                if (!rmi_client.user.KiemTraSo(dienThoai)) {
                    JOptionPane.showMessageDialog(null, "Số điện thoại phải là số!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    int res = rmi_client.user.ThemKH(kH);
                    if (res > 0) {
                        JOptionPane.showMessageDialog(null, "Lưu thành công", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
                        dtm.setRowCount(0);
                        Show_Data_KH();
                        
                    } else {
                        JOptionPane.showMessageDialog(null, "Đã tồn tại Mã khách hàng", "Warning", JOptionPane.WARNING_MESSAGE);
                    }
                }
            } catch (RemoteException ex) {
                Logger.getLogger(frmKhachHang.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Bạn chưa điền đủ thông tin", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String Search = txtTimKiem.getText();
        try {

            ArrayList<KhachHang> list = rmi_client.user.TimKiem(Search);
            dtm = (DefaultTableModel) tableKhachHang.getModel();//thêm dữ liêu vào tablenguoidung
            dtm.setRowCount(0);
            Object[] obj = new Object[5];
            for (int i = 0; i < list.size(); i++) {
                obj[0] = list.get(i).getMaKH();
                obj[1] = list.get(i).getTenKH();
                obj[2] = list.get(i).getDiaChiKH();
                obj[3] = list.get(i).getDienThoaiKH();
                obj[4] = list.get(i).getMaLoaiKH();
                dtm.addRow(obj);//Thêm một dòng vào dm
            }
        } catch (RemoteException e) {
            JOptionPane.showMessageDialog(null, "Lỗi " + e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnTimKiemActionPerformed

